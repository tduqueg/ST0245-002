public class Punto3 {

    public static int suma (int[]array){
        int sum=0;
        for(int i=0;i< sum ;i++){
            sum+=array[i];
        }
        return sum;
    }

    public static void mul (int num){
        for(int i=1;i<=10;i++){
            System.out.println(num+" x "+i+" = "+(num * i ));
        }
    }

}

